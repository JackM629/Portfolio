    private void viewCurrentBookingsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewCurrentBookingsButtonActionPerformed
        // TODO add your handling code here:
        String memberIDNumber = memberIDNumberTextField.getText();
        String error = sysMan.validateMemberIDNumber(memberIDNumber);
        if (error != null) {
            JOptionPane.showMessageDialog(viewCurrentBookingsPanel, error, "Error", JOptionPane.ERROR_MESSAGE);
        }
        else {
            String results = sysMan.viewCurrentBookings(memberIDNumber);
            if (results != null) {
                currentBookingsTextBox.setText(results);
            }
        }
        
    }//GEN-LAST:event_viewCurrentBookingsButtonActionPerformed

