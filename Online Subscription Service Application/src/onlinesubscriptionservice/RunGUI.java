    private void findItemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findItemButtonActionPerformed
        // TODO add your handling code here:
        String searchID = bookItemIDNumberTextField.getText();
        bookItemPreviewTextBox.setText("");
        String error = sysMan.validateFindItemSearchTerm(searchID);
        if (error != null) {
            JOptionPane.showMessageDialog(requestBookingPanel, error, "Error", JOptionPane.ERROR_MESSAGE);
        }
        else {
            String locatedItem = sysMan.findItem(searchID);
            if (locatedItem == null) {
               JOptionPane.showMessageDialog(requestBookingPanel, "No items found", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            else {
               bookItemPreviewTextBox.setText(locatedItem);
            }
        }
    }//GEN-LAST:event_findItemButtonActionPerformed

